CREATE DATABASE IF NOT EXISTS kids_learning;

USE kids_learning;

-- 删除旧表
DROP TABLE IF EXISTS poetry;
DROP TABLE IF EXISTS characters;
DROP TABLE IF EXISTS english;
DROP TABLE IF EXISTS pinyin;
DROP TABLE IF EXISTS idiom;
DROP TABLE IF EXISTS drawing;
DROP TABLE IF EXISTS math;

-- 创建诗歌表
CREATE TABLE IF NOT EXISTS poetry (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(255) NOT NULL,
    author VARCHAR(255) NOT NULL,
    content TEXT NOT NULL,
    explanation TEXT,
    dynasty VARCHAR(20) NOT NULL,
    learned BOOLEAN DEFAULT FALSE, -- 是否已学
    tags TEXT -- 标签，用逗号分隔
);

-- 创建其他表
CREATE TABLE IF NOT EXISTS characters (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    `character` VARCHAR(10) NOT NULL,
    pinyin VARCHAR(50) NOT NULL,
    meaning TEXT NOT NULL,
    stroke_order TEXT,
    category VARCHAR(50) NOT NULL, -- 类别：数字、自然、动物、植物、身体、颜色、方位、时间、人称、动作
    learned BOOLEAN DEFAULT FALSE -- 是否已学
);

CREATE TABLE IF NOT EXISTS english (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    word VARCHAR(100) NOT NULL,
    pronunciation VARCHAR(100) NOT NULL,
    meaning TEXT NOT NULL,
    example TEXT,
    type VARCHAR(50) NOT NULL, -- animal(动物), fruit(水果), color(颜色), number(数字), body(身体), food(食物), nature(自然), family(家庭), school(学校), action(动作), adjective(形容词)
    learned BOOLEAN DEFAULT FALSE -- 是否已学
);

CREATE TABLE IF NOT EXISTS pinyin (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    pinyin VARCHAR(50) NOT NULL,
    `character` VARCHAR(10) NOT NULL,
    type VARCHAR(10) NOT NULL, -- vowel(元音) or consonant(辅音)
    description TEXT,
    learned BOOLEAN DEFAULT FALSE -- 是否已学
);

CREATE TABLE IF NOT EXISTS idiom (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    idiom VARCHAR(50) NOT NULL,
    pinyin VARCHAR(100) NOT NULL,
    meaning TEXT NOT NULL,
    origin TEXT,
    learned BOOLEAN DEFAULT FALSE -- 是否已学
);

CREATE TABLE IF NOT EXISTS drawing (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    image_path VARCHAR(255),
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS math (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(255) NOT NULL,
    content TEXT NOT NULL,
    solution TEXT NOT NULL
);

